package practice;

public class Dog {

  private String name;
  private String type;
  private int age;

  public Dog(String name, String type, int age) {
    this.name = name;
    this.type = type;
    this.age = age;
  }

  public void setName(String type) {
    this.type = name;
  }

  public String getName() {
    return this.name;
  }

  public void setType(String type) {
    this.type = type;
  }

  public String getType() {
    return this.type;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public int getAge() {
    return this.age;
  }
}
